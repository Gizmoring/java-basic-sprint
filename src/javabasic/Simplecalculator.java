package javabasic;

public class Simplecalculator {
    public int num1;
    public int num2;


    public int sumcalclulator(int num1, int num2){
        return num1 + num2;
    }
    public int subtractcalculator(int num1, int num2){
        return num1 - num2;
    }
    public int devidecalculator(int num1, int num2){
        return num1 / num2;
    }
    public int multiplycalculator(int num1, int num2){
        return num1 * num2;
    }
}

